/**
 * Options for creating a new {@link Run} via the builder pattern.
 *
 * Every field is optional — sensible defaults are applied automatically.
 *
 * @example
 * ```ts
 * const r = run({ sessionId: "sess_123", conversational: true });
 * ```
 */
export type RunOptions = {
  /** Custom run identifier. Defaults to a random UUID. */
  runId?: string;
  /**
   * Group multiple runs into a logical session (e.g. a single user
   * conversation). Runs sharing a `sessionId` appear together in the
   * dashboard timeline.
   */
  sessionId?: string;
  /**
   * Mark this run as part of a multi-turn conversation. When `true`,
   * the dashboard links sequential runs in the same session as a
   * continuous thread.
   */
  conversational?: boolean;
  /** Override the run start time. Defaults to `new Date()`. */
  startTime?: Date;
  /**
   * Auto-flush timeout in milliseconds. If the run is not ended within
   * this duration, it will be automatically flushed with an error status.
   *
   * Set to `0` to disable. Can also be set globally via
   * {@link ClientConfig.runTimeout | configure({ runTimeout })}.
   *
   * @defaultValue `1_200_000` (20 minutes)
   */
  timeout?: number;
};

/**
 * Options for creating a new {@link Step} via `run.step()`.
 *
 * @example
 * ```ts
 * const s = r.step({ stepId: "step_abc" });
 * ```
 */
export type StepOptions = {
  /** Custom step identifier. Defaults to a random UUID. */
  stepId?: string;
  /** Override the step start time. Defaults to `new Date()`. */
  startTime?: Date;
};

/**
 * Options for creating a new {@link ToolCall} via `run.toolCall()`.
 *
 * @example
 * ```ts
 * const tc = r.toolCall({ name: "search", toolCallId: "tc_xyz" });
 * ```
 */
export type ToolCallOptions = {
  /** Custom tool call identifier. Defaults to a random UUID. */
  toolCallId?: string;
  /** The name of the tool being invoked (e.g. `"search"`, `"get_weather"`). */
  name?: string;
  /** Override the tool call start time. Defaults to `new Date()`. */
  startTime?: Date;
};

/**
 * Token usage breakdown for a single LLM step.
 *
 * All fields are optional — report whichever counts your provider exposes.
 *
 * @example
 * ```ts
 * s.tokens({ uncached: 120, cached: 30, completion: 45 });
 * ```
 */
export type TokenUsage = {
  /** Number of prompt tokens that were **not** served from cache. */
  uncached?: number;
  /** Number of prompt tokens served from cache (e.g. prompt caching). */
  cached?: number;
  /** Number of tokens generated by the model in the completion. */
  completion?: number;
};

/**
 * Specifies which model was used for an LLM step.
 *
 * Pass a plain string when the requested and actually-used model are the same.
 * Use the object form when they differ (e.g. requesting `"gpt-4o"` but the
 * provider routes to a specific snapshot like `"gpt-4o-2024-08-06"`).
 *
 * @example
 * ```ts
 * // Shorthand — same model for both
 * s.model("gpt-4o");
 *
 * // Explicit requested vs. used
 * s.model({ requested: "gpt-4o", used: "gpt-4o-2024-08-06" });
 * ```
 */
export type ModelConfig = string | { requested?: string; used?: string };

